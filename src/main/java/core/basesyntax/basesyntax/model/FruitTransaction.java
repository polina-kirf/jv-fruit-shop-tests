package core.basesyntax.basesyntax.model;

import java.util.Objects;

public class FruitTransaction {
    private static final String CODE_B = "b";
    private static final String CODE_S = "s";
    private static final String CODE_P = "p";
    private static final String CODE_R = "r";

    private final Operation operation;
    private final String fruit;
    private final int quantity;

    public FruitTransaction(Operation operation, String fruit, int quantity) {
        this.operation = operation;
        this.fruit = fruit;
        this.quantity = quantity;
    }

    public Operation getOperation() {
        return operation;
    }

    public String getFruit() {
        return fruit;
    }

    public int getQuantity() {
        return quantity;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        FruitTransaction that = (FruitTransaction) o;
        return quantity == that.quantity && operation == that.operation
                && Objects.equals(fruit, that.fruit);
    }

    @Override
    public int hashCode() {
        return Objects.hash(operation, fruit, quantity);
    }

    public enum Operation {
        BALANCE(CODE_B),
        SUPPLY(CODE_S),
        PURCHASE(CODE_P),
        RETURN(CODE_R);

        private final String code;

        Operation(String code) {
            this.code = code;
        }

        public String getCode() {
            return code;
        }

        public static Operation fromCode(String code) {
            for (Operation op : values()) {
                if (op.getCode().equals(code)) {
                    return op;
                }
            }
            throw new RuntimeException("Unknown operation: " + code);
        }
    }
}
